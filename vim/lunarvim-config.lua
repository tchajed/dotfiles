vim.api.nvim_create_autocmd("FileType", {
    pattern = "markdown",
    callback = function()
        vim.cmd([[
            syntax region match start=/\\$\\$/ end=/\\$\\$/
        ]])
    end,
})
vim.api.nvim_create_autocmd("FileType", {
    pattern = "markdown",
    callback = function()
        vim.cmd([[
            syntax match math '\\$[^$].\{-}\$'
        ]])
    end,
})
